import 'package:flutter/foundation.dart';
import 'package:flutter/material.dart';
import 'package:flutter_screenutil/flutter_screenutil.dart';
import 'package:flutter_inappwebview/flutter_inappwebview.dart';

import '../../../../core/constants/api_constants.dart';
import '../../../../l10n/app_localizations.dart';

class PrivacyWebViewPage extends StatefulWidget {
  final String type;

  const PrivacyWebViewPage({
    super.key,
    required this.type
  });

  @override
  State<PrivacyWebViewPage> createState() => _PrivacyWebViewPageState();
}

class _PrivacyWebViewPageState extends State<PrivacyWebViewPage> {
  InAppWebViewController? webViewController;
  double progress = 0;
  bool isLoading = true;
  String? errorMessage;
  late String initialUrl;
  late String title;

  @override
  void initState() {
    super.initState();
    if (widget.type == 'privacy') {
      initialUrl = ApiConstant.WebFrameGetPrivacyURL;
    } else if (widget.type == 'public') {
      initialUrl = ApiConstant.WebFrameGetPublicURL;
    } else {
      initialUrl = ApiConstant.WebFrameGetPublicMobileURL;
    }
    _initializeWebView();
  }

  @override
  void didChangeDependencies() {
    super.didChangeDependencies();
    if (widget.type == 'privacy') {
      title = AppLocalizations.of(context)!.privacyTitle;
    } else if (widget.type == 'public') {
      title = AppLocalizations.of(context)!.publicTitle;
    } else {
      title = AppLocalizations.of(context)!.publicMobileTitle;
    }
  }

  Future<void> _initializeWebView() async {
    if (!kIsWeb && defaultTargetPlatform == TargetPlatform.android) {
      await InAppWebViewController.setWebContentsDebuggingEnabled(kDebugMode);
    }
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text(title),
        backgroundColor: Colors.white,
        foregroundColor: Colors.black,
        elevation: 0,
        leading: IconButton(
          icon: const Icon(Icons.close),
          onPressed: () => Navigator.of(context).pop(),
        ),
      ),
      body: Stack(
        children: [
          InAppWebView(
            initialUrlRequest: URLRequest(url: WebUri(initialUrl)),
            onWebViewCreated: (controller) {
              webViewController = controller;
            },
            shouldOverrideUrlLoading: (controller, navigationAction) async {
              final uri = navigationAction.request.url;

              if (uri != null) {
                print('üîÑ Navigation request: ${uri.toString()}');
                print('üìã Navigation type: ${navigationAction.navigationType}');
                print('üéØ Is main frame: ${navigationAction.isForMainFrame}');

                // –†–∞–∑—Ä–µ—à–∞–µ–º –≤—Å–µ –Ω–∞–≤–∏–≥–∞—Ü–∏–∏ –¥–ª—è –ø—Ä–∞–≤–∏–ª—å–Ω–æ–π —Ä–∞–±–æ—Ç—ã —Ä–µ–¥–∏—Ä–µ–∫—Ç–æ–≤
                // –û—Å–æ–±–µ–Ω–Ω–æ –≤–∞–∂–Ω–æ –¥–ª—è –ø–ª–∞—Ç–µ–∂–Ω—ã—Ö —Å–∏—Å—Ç–µ–º
                return NavigationActionPolicy.ALLOW;
              }

              return NavigationActionPolicy.ALLOW;
            },
            onLoadStart: (controller, url) {
              setState(() {
                isLoading = true;
                errorMessage = null;
              });
              print('üöÄ Loading started: ${url?.toString()}');
            },
            onUpdateVisitedHistory: (controller, url, androidIsReload) {
              print('üìç Visited: ${url?.toString()} (reload: $androidIsReload)');
            },
            onLoadStop: (controller, url) async {
              print('‚úÖ Loading finished: ${url?.toString()}');
              setState(() {
                isLoading = false;
              });

              // –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –¥–ª—è –ø–æ–¥–¥–µ—Ä–∂–∫–∏ —Ä–µ–¥–∏—Ä–µ–∫—Ç–æ–≤ –∏ touch —Å–æ–±—ã—Ç–∏–π
              await controller.evaluateJavascript(source: """
                // –ü—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–æ –Ω–∞—Å—Ç—Ä–∞–∏–≤–∞–µ–º viewport –¥–ª—è –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π –ø–æ–¥–¥–µ—Ä–∂–∫–∏ –∑—É–º–∞
                var metaTag = document.querySelector('meta[name="viewport"]');
                if (metaTag) {
                  metaTag.setAttribute('content', 'width=device-width, initial-scale=1.0, maximum-scale=10.0, minimum-scale=0.1, user-scalable=yes, shrink-to-fit=no, viewport-fit=cover');
                } else {
                  var meta = document.createElement('meta');
                  meta.name = 'viewport';
                  meta.content = 'width=device-width, initial-scale=1.0, maximum-scale=10.0, minimum-scale=0.1, user-scalable=yes, shrink-to-fit=no, viewport-fit=cover';
                  document.getElementsByTagName('head')[0].appendChild(meta);
                }

                // –û–±–µ—Å–ø–µ—á–∏–≤–∞–µ–º –ø—Ä–∞–≤–∏–ª—å–Ω—É—é —Ä–∞–±–æ—Ç—É —Ñ–æ—Ä–º –∏ —Å—Å—ã–ª–æ–∫ –¥–ª—è —Ä–µ–¥–∏—Ä–µ–∫—Ç–æ–≤
                document.addEventListener('click', function(e) {
                  var target = e.target;

                  // –†–∞–∑—Ä–µ—à–∞–µ–º –∫–ª–∏–∫–∏ –ø–æ —Å—Å—ã–ª–∫–∞–º –∏ –∫–Ω–æ–ø–∫–∞–º
                  if (target.tagName === 'A' || target.tagName === 'BUTTON' || target.type === 'submit') {
                    // –ù–µ –±–ª–æ–∫–∏—Ä—É–µ–º –Ω–∞–≤–∏–≥–∞—Ü–∏—é
                    return true;
                  }
                }, false);

                // –†–∞–∑—Ä–µ—à–∞–µ–º –æ—Ç–ø—Ä–∞–≤–∫—É —Ñ–æ—Ä–º
                document.addEventListener('submit', function(e) {
                  // –ù–µ –±–ª–æ–∫–∏—Ä—É–µ–º –æ—Ç–ø—Ä–∞–≤–∫—É —Ñ–æ—Ä–º
                  return true;
                }, false);

                // –ê–≥—Ä–µ—Å—Å–∏–≤–Ω–∞—è –Ω–∞—Å—Ç—Ä–æ–π–∫–∞ CSS –¥–ª—è touch —Å–æ–±—ã—Ç–∏–π
                var style = document.createElement('style');
                style.innerHTML = `
                  html, body {
                    touch-action: pan-x pan-y pinch-zoom !important;
                    -webkit-user-select: none !important;
                    -webkit-touch-callout: none !important;
                    overflow: auto !important;
                  }
                  * {
                    touch-action: pan-x pan-y pinch-zoom !important;
                    -webkit-user-select: none !important;
                    -webkit-touch-callout: none !important;
                  }
                  img, canvas, svg, .map-container {
                    touch-action: pan-x pan-y pinch-zoom !important;
                    user-select: none !important;
                    -webkit-user-drag: none !important;
                    pointer-events: auto !important;
                  }
                  button, a, input[type="submit"], input[type="button"] {
                    touch-action: manipulation !important;
                    pointer-events: auto !important;
                    cursor: pointer !important;
                  }
                  form {
                    pointer-events: auto !important;
                  }
`;
                document.head.appendChild(style);

                // –ü—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–æ –≤–∫–ª—é—á–∞–µ–º –≤—Å–µ touch —Å–æ–±—ã—Ç–∏—è
                var touchEvents = ['touchstart', 'touchmove', 'touchend', 'touchcancel'];
                touchEvents.forEach(function(eventType) {
                  document.addEventListener(eventType, function(e) {
                    // –ù–µ –±–ª–æ–∫–∏—Ä—É–µ–º —Å–æ–±—ã—Ç–∏—è, –ø–æ–∑–≤–æ–ª—è–µ–º –±—Ä–∞—É–∑–µ—Ä—É –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å –∏—Ö –Ω–∞—Ç–∏–≤–Ω–æ
                  }, {
                    passive: true,
                    capture: false
                  });
                });

                // –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–∞—è –Ω–∞—Å—Ç—Ä–æ–π–∫–∞ –¥–ª—è Android —ç–º—É–ª—è—Ç–æ—Ä–∞
                if (navigator.userAgent.includes('Android')) {
                  // –ü—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–æ –≤–∫–ª—é—á–∞–µ–º –≤—Å–µ pointer events
                  var pointerEvents = ['pointerdown', 'pointermove', 'pointerup', 'pointercancel'];
                  pointerEvents.forEach(function(eventType) {
                    document.addEventListener(eventType, function(e) {
                      // –ü–æ–∑–≤–æ–ª—è–µ–º –Ω–∞—Ç–∏–≤–Ω—É—é –æ–±—Ä–∞–±–æ—Ç–∫—É
                    }, { passive: true });
                  });

                  // –í–∫–ª—é—á–∞–µ–º wheel —Å–æ–±—ã—Ç–∏—è –¥–ª—è zoom —á–µ—Ä–µ–∑ scroll
                  document.addEventListener('wheel', function(e) {
                    if (e.ctrlKey) {
                      // –ü–æ–∑–≤–æ–ª—è–µ–º zoom —á–µ—Ä–µ–∑ Ctrl+wheel
                    }
                  }, { passive: true });
                }


                // –ü—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–æ –≤–∫–ª—é—á–∞–µ–º zoom –¥–ª—è –≤—Å–µ—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤
                document.documentElement.style.zoom = 'normal';
                document.body.style.zoom = 'normal';

                console.log('Enhanced WebView support initialized for redirects and touch');
                console.log('User agent:', navigator.userAgent);
                console.log('Touch support:', 'ontouchstart' in window);
                console.log('Pointer support:', 'onpointerdown' in window);
                console.log('Current URL:', window.location.href);
              """);
            },
            onProgressChanged: (controller, progress) {
              setState(() {
                this.progress = progress / 100;
              });
            },
            onReceivedError: (controller, request, error) {
              setState(() {
                isLoading = false;
                errorMessage = '${AppLocalizations.of(context)!.loadingError}: ${error.description}';
              });
            },
            onReceivedHttpError: (controller, request, errorResponse) {
              print('HTTP Error: ${errorResponse.statusCode} - ${errorResponse.reasonPhrase}');
              print('Failed URL: ${request.url}');

              // –ò–≥–Ω–æ—Ä–∏—Ä—É–µ–º 404 –æ—à–∏–±–∫–∏ –¥–ª—è —Ä–µ—Å—É—Ä—Å–æ–≤ (CSS, JS, –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è)
              final url = request.url?.toString() ?? '';
              final isResource = url.contains('.css') ||
                  url.contains('.js') ||
                  url.contains('.svg') ||
                  url.contains('.png') ||
                  url.contains('.jpg') ||
                  url.contains('.ico');

              // –ò–≥–Ω–æ—Ä–∏—Ä—É–µ–º 404 –æ—à–∏–±–∫–∏ –¥–ª—è —Ä–µ—Å—É—Ä—Å–æ–≤ –∏ 400 –æ—à–∏–±–∫–∏ (–ø—É—Å—Ç—å —Å–∞–π—Ç —Å–∞–º –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç)
              if ((isResource && errorResponse.statusCode == 404) ||
                  errorResponse.statusCode == 400) {
                return; // –ù–µ –ø–æ–∫–∞–∑—ã–≤–∞–µ–º –æ—à–∏–±–∫—É
              }

              // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –æ—à–∏–±–∫—É —Ç–æ–ª—å–∫–æ –¥–ª—è –∫—Ä–∏—Ç–∏—á–µ—Å–∫–∏—Ö —Å–ª—É—á–∞–µ–≤
              setState(() {
                isLoading = false;
                errorMessage = '${AppLocalizations.of(context)!.httpError} ${errorResponse.statusCode}: ${errorResponse.reasonPhrase}';
              });
            },
            onConsoleMessage: (controller, consoleMessage) {
              print('Console ${consoleMessage.messageLevel}: ${consoleMessage.message}');
            },
          ),
          if (isLoading)
            Container(
              color: Colors.white,
              child: Center(
                child: Column(
                  mainAxisAlignment: MainAxisAlignment.center,
                  children: [
                    const CircularProgressIndicator(),
                    SizedBox(height: 16.h),
                    Text(
                      AppLocalizations.of(context)!.loading,
                      style: TextStyle(fontSize: 16.sp),
                    ),
                    if (progress > 0)
                      Padding(
                        padding: EdgeInsets.symmetric(horizontal: 32.w, vertical: 8.h),
                        child: LinearProgressIndicator(value: progress),
                      ),
                  ],
                ),
              ),
            ),
          if (!isLoading && errorMessage != null)
            Container(
              color: Colors.white,
              child: Center(
                child: Column(
                  mainAxisAlignment: MainAxisAlignment.center,
                  children: [
                    Icon(
                      Icons.error_outline,
                      size: 64.sp,
                      color: Colors.red,
                    ),
                    SizedBox(height: 16.h),
                    Text(
                      errorMessage!,
                      style: TextStyle(fontSize: 16.sp),
                      textAlign: TextAlign.center,
                    ),
                    SizedBox(height: 16.h),
                    ElevatedButton(
                      onPressed: () {
                        webViewController?.reload();
                      },
                      child: Text(AppLocalizations.of(context)!.tryAgain),
                    ),
                  ],
                ),
              ),
            ),
        ],
      ),
    );
  }
}